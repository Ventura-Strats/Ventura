#!/usr/bin/env Rscript

####################################################################################################
### Script description:
### Generates crontabs for all machines from one synthetic common source file
####################################################################################################

####################################################################################################
### Environment variables initialization
####################################################################################################
start_time <- Sys.time(); 
max_time_hours <- 0.25;

script_arguments <- commandArgs(trailingOnly = TRUE);
pid <- NULL;
if (length(script_arguments) > 0) {
  pid <- as.integer(script_arguments[1]);
}

script_name <- "Generate_Crontab"; 

####################################################################################################
### Script sub functions
####################################################################################################

####################################################################################################
### Script
####################################################################################################
Script <- function() {
  B.generateCrontabUser("user")
  I.completeStage(1)
  B.generateCrontabUser("su")
  I.completeStage(2)
}

####################################################################################################
### Execution
####################################################################################################
source("Init.R"); I.executeScript()
